    `Presidency` INT,
    `President` VARCHAR(100),
    `Wikipedia_entry` TEXT,
    `Took_office` DATE,
    `Left_office` DATE,
    `Party` VARCHAR(100),
    `Home_state` VARCHAR(100),
    `Occupation` VARCHAR(100),
    `College` VARCHAR(100),
    `Age_when_took_office` INT,
    `Birth_date` DATE,
    `Birthplace` VARCHAR(150),
    `Death_date` DATE,
    `Location_death` VARCHAR(150),
    `Image` VARCHAR(50)
);